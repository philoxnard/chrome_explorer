class Monster:
    """
    A class to model a monster
    """

    def __init__(self):
        self.name = ""
        self.nickname = ""
        self.level = 0
        self.experience = 0
        self.strength = 0
        self.agility = 0
        self.intelligence = 0
        self.constitution = 0
        self.charisma = 0
